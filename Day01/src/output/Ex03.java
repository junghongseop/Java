package output;

public class Ex03 {
	public static void main(String[] args) {
		// escape sequence:문자열 내에서 특수하 형태의 문자들
		// - \ (백슬래시)와 결합되어 사용됨
		// 문자열:" "(쌍따옴표)로 묶인 데이터
		// 문자: ' '(홀따옴표)로 묶인 데이터.
		
		System.out.println("Hello\nWorld!!");
		System.out.println("Hello \t\t World");
	}
}
